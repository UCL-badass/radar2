package uk.ac.ucl.cs.radar.model;

 class Node {
	String label_;
	String shape_;
	String style_;
	public void setLabel (String label){
		label_ =label;
	}
	public String getLabel (){
		return label_;
	}
	public void setShape (String shape){
		shape_ =shape;
	}
	public String getShape (){
		return shape_;
	}
	public void setStyle (String style){
		style_ =style;
	}
	public String getStyle (){
		return style_;
	}
}
